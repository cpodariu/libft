#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	size_t i;

	if (!dest || !src)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = src[i];
	return (dest);
}
